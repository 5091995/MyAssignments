package week1.day2;

public class EvenOrOdd {

	public static void main(String[] args) {
	
		for(int a=1;a<=10;a++)
		{
		
				if(a%2==0)
				{
					System.out.println("Even number:"+a);//+a is like append the value of a with the string
				}
				else 			{
					System.out.println("odd number:"+a);
				}
		
		}
	}

}
